package module1.chapter2;

public class ForLoopExercise {
    public static void main(String[] args) {

        for (int i=1; i<=5; i++){
            System.out.println(i*3);
        }
        System.out.println();
        
        for (int i=9; i>5; i--){
            System.out.println(i-2);
        }
    }
}
