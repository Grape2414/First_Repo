package module1.chapter2;

public class Lesson13whileLoop {
    public static void main(String[] args) {
        int a=0;
        while (a <= 5){
            System.out.println(a);
            a++;
        }
        System.out.println(a);

        int v=21;
        while (v> 10){
            System.out.println(v);
            v--;
        }

    }
}
