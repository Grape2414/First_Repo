package module1.chapter1.operator;

public class Lesson7AssignmentOperator {
    public static void main(String[] args) {
        int a=4;
        int b=5;
        b +=a;
        System.out.println(b);

        int c=3;
        c+=4;
        System.out.println(c);

        int d= 2;
        d/=2;
        System.out.println(d);

        int e=18;
        e-=5;
        System.out.println(e);
    }
}
